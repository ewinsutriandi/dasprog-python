# Operator = mengisi variabel dengan suatu nilai
x = 5
print(f"Sekarang nilai x adalah {x}")

# Operator +=
x += 2 # bermakna x = x + 2
print(f"Dengan ekspresi x += 2 nilai x sekarang adalah {x}")

# Operator -=
x -= 3 # bermakna x = x - 3
print(f"Dengan ekspresi x -= 3 nilai x sekarang adalah {x}")

# Operator *=
x *= 2 # bermakna x = x * 2
print(f"Dengan ekspresi x *= 2 nilai x sekarang adalah {x}")

# Operator /=
x /= 4 # bermakna x = x / 4
print(f"Dengan ekspresi x /= 4 nilai x sekarang adalah {x}")