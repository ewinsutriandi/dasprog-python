# Operator aritmatika 

# operator tambah pada tipe bilangan (int / float)
b_1 = 7
b_2 = 3
hsl_tbh = b_1 + b_2
print(f"Ekspresi {b_1} + {b_2} bernilai {hsl_tbh}")

# operator kurang (-)
hsl_krg = b_1 - b_2
print(f"Ekspresi {b_1} - {b_2} bernilai {hsl_krg}")

# operator kali (*)
hsl_kl = b_1 * b_2
print(f"Ekspresi {b_1} * {b_2} bernilai {hsl_kl}")

# operator pangkat (**)
hsl_pkt = b_1 ** b_2
print(f"Ekspresi {b_1} ** {b_2} bernilai {hsl_pkt}")

# operator bagi (/)
hsl_bg = b_1 / b_2
print(f"Ekspresi {b_1} / {b_2} bernilai {hsl_bg}")

# operator floor (//), bagi dibulatkan ke bawah
hsl_bgf = b_1 // b_2
print(f"Ekspresi {b_1} // {b_2} bernilai {hsl_bgf}")

# operator modulus (%) / sisa hasil bagi
hsl_mod = b_1 % b_2
print(f"Ekspresi {b_1} % {b_2} bernilai {hsl_mod}")

# Kasus khusus operator tambah pada tipe str
t_1 = "Hello"
t_2 = "World"
hsl_tbh_t = t_1 + t_2
print(f"Ekspresi {t_1} + {t_2} bernilai {hsl_tbh_t}")

# Kasus khusus operator kali pada tipe str dan int
t_1 = "Hello "
hsl_kali_tint = t_1 * 3
print(f"Ekspresi {t_1} * 3 bernilai {hsl_kali_tint}")
