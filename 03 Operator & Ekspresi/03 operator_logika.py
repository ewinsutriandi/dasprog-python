# Operator logika: and, or, not

benar = True
salah = False

hasil = benar or salah
print(f"Ekspresi {benar} or {salah} bernilai {hasil} ")

hasil = benar and salah
print(f"Ekspresi {benar} and {salah} bernilai {hasil} ")

hasil = not benar
print(f"Ekspresi not {benar} bernilai {hasil} ")

hasil = not salah
print(f"Ekspresi not {salah} bernilai {hasil} ")

