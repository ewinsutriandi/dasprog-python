# Operator relasional: < > <= >= == != 
bil_1 = 3
bil_2 = 7

hasil = bil_1 < bil_2
print(f"Ekspresi {bil_1} < {bil_2} bernilai {hasil} ")

hasil = bil_1 > bil_2
print(f"Ekspresi {bil_1} > {bil_2} bernilai {hasil} ")

hasil = bil_1 == bil_2
print(f"Ekspresi {bil_1} == {bil_2} bernilai {hasil} ")

hasil = bil_1 != bil_2
print(f"Ekspresi {bil_1} != {bil_2} bernilai {hasil} ")



